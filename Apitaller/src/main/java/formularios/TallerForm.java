    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try {
            
            URL url = new URL("http://localhost:5062/api/Taller");
            HttpURLConnection conn = (HttpURLConnection) url.openConnection();
            conn.setRequestMethod("GET");
             System.out.println("Conexion   "+conn);
            conn.connect();
            int responseCode = conn.getResponseCode();
            System.out.println("Respuesta de API "+responseCode);
            if (responseCode == 200) {
                Scanner scanner = new Scanner(url.openStream());
                StringBuilder jsonText = new StringBuilder();

                while (scanner.hasNext()) {
                    jsonText.append(scanner.nextLine());
                }
                System.out.println("jsontext "+jsonText );
                scanner.close();

             
                Gson gson = new Gson();
                Taller[]Taller= gson.fromJson(jsonText.toString(), Taller[].class);
                System.out.println("Taller  "+Taller );
                

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0); 

                for (Taller taller  : Taller) {
                    model.addRow(new Object[]{
                        taller.getid(),
                        taller.getnombre(),
                        taller.getdireccion(),
                        
                    });
                }

            } else {
                System.out.println("Error en la conexi√≥n: " + responseCode);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }          // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

